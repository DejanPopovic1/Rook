void printMoveRuleValues(){
    T_bitboard **rays = createRays();
    T_bitboard **jumps = createJumps();
    T_bitboard **steps = createSteps();
    FILE *fp;
    fp = fopen("movementRules.txt", "w");
    char str[1000];

    fputs("{\n", fp);
    for(int i = 0; i < 8; i++){
        fputs("\t{", fp);
        for(int j = 0; j < 64; j++){
            sprintf(str, "%llu", rays[i][j]);
            fputs(str, fp);
            if(j != 63){
                fputs(", ", fp);
            }
        }
        if(i != 7){
            fputs("},\n", fp);
        }
        else if(i == 7){
            fputs("}\n", fp);
        }
    }
    fputs("};\n\n", fp);

    fputs("{\n", fp);
    for(int i = 0; i < 8; i++){
        fputs("\t{", fp);
        for(int j = 0; j < 64; j++){
            sprintf(str, "%llu", jumps[i][j]);
            fputs(str, fp);
            if(j != 63){
                fputs(", ", fp);
            }
        }
        if(i != 7){
            fputs("},\n", fp);
        }
        else if(i == 7){
            fputs("}\n", fp);
        }
    }
    fputs("};\n\n", fp);

    fputs("{\n", fp);
    for(int i = 0; i < 8; i++){
        fputs("\t{", fp);
        for(int j = 0; j < 64; j++){
            sprintf(str, "%llu", steps[i][j]);
            fputs(str, fp);
            if(j != 63){
                fputs(", ", fp);
            }
        }
        if(i != 7){
            fputs("},\n", fp);
        }
        else if(i == 7){
            fputs("}\n\n", fp);
        }
    }
    fputs("};\n\n", fp);

    fclose(fp);
}

